package com.exactpro.cradle.testevents;

import java.io.Serializable;

/**
 * Holds ID of a test event stored in Cradle
 */
public class StoredTestEventId implements Serializable
{
	private static final long serialVersionUID = 8372826721377298138L;
	
	private final String id;
	
	public StoredTestEventId(String id)
	{
		this.id = id;
	}
	
	
	public String getId()
	{
		return id;
	}
	
	
	@Override
	public String toString()
	{
		return id;
	}
	
	@Override
	public int hashCode()
	{
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj)
	{
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		StoredTestEventId other = (StoredTestEventId) obj;
		if (id == null)
		{
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
}
